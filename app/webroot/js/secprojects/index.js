function add(url)
{	
	var w = window.open(url, 'agregar', 
	        'scrollbars=no,resizable=no,width=360,height=480,top=100,left=450,status=no,location=no,toolbar=no,menubar=no');
}

function editar(url)
{	
	var w = window.open(url, 'editar', 
	        'scrollbars=no,resizable=yes,width=360,height=480,top=100,left=450,status=no,location=no,toolbar=no');
}

