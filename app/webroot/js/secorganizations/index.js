function add(url)
{	
	var w = window.open(url, 'agregar', 
	        'scrollbars=no,resizable=yes,width=360,height=520,top=90,left=450,status=no,location=no,toolbar=no,menubar=no');
}

function editar(url)
{	
	var w = window.open(url, 'editar', 
	        'scrollbars=no,resizable=yes,width=360,height=520,top=90,left=450,status=no,location=no,toolbar=no');
}

function mostrar(url)
{	
	var w = window.open(url, 'editar', 
	        'scrollbars=no,resizable=yes,width=400,height=480,top=90,left=450,status=no,location=no,toolbar=no');
}