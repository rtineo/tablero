function add(url)
{	var w = window.open(url, 'agregar', 
	        'scrollbars=no,resizable=yes,width=380,height=400,top=100,left=450,status=no,location=no,toolbar=no,menubar=no');
}

function editar(url)
{	var w = window.open(url, 'editar', 
	        'scrollbars=no,resizable=yes,width=400,height=400,top=100,left=450,status=no,location=no,toolbar=no');
}

function mostrar(url)
{	var w = window.open(url, 'editar', 
	        'scrollbars=no,resizable=yes,width=420,height=350,top=100,left=450,status=no,location=no,toolbar=no');
}